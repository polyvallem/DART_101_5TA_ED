/// Los comentarios son una parte importante del código.
/// Permiten a los programadores documentar su código
/// y explicar su funcionamiento.
/// En Dart, los comentarios se pueden hacer de dos maneras:
/// 1. Comentarios de una sola línea: Se utilizan dos barras diagonales (//)
///    para comentar una sola línea de código.
/// 2. Comentarios de varias líneas: Se utilizan barras diagonales y asteriscos (/* */)
///    para comentar varias líneas de código.
///
/// Ejemplo de comentario de una sola línea:
/// // Este es un comentario de una sola línea
/// print('Hola Mundo'); // Este es un comentario al final de una línea
///
/// Ejemplo de comentario de varias líneas:
/// /*
/// Este es un comentario de varias líneas.
/// Puede abarcar varias líneas.
/// Se utiliza para explicar el código de manera más detallada.
/// */


/// En resumen, los comentarios son una herramienta valiosa
/// para documentar el código y facilitar su comprensión.
/// Es importante utilizarlos de manera adecuada
/// para que otros programadores (o tú mismo en el futuro)
/// puedan entender el propósito y funcionamiento del código.
/// Recuerda que los comentarios no afectan la ejecución del programa.
/// Son simplemente anotaciones para los programadores.
/// 
/// 

